// Symbol: Unique, immutable identifier (e.g., Symbol("id")).
// BigInt: Large integers beyond Number limits (e.g., 123n).

// Non-Primitive (Reference) Types:
// Object: Key-value pairs (e.g., { name: "John" }).
// Array: Ordered list (e.g., [1, 2, 3]).
