// unary operator
// postfix increment: first it prints then it increments and prints 

// Using let to declare a variable
let a = 5;

// 1. Postfix Increment (a++): Returns value first, then increments
console.log("Postfix a++:", a++); // Output: 5 (returns a, then a becomes 6)
console.log("After a++:", a); // Output: 6

// 2. Postfix Decrement (a--): Returns value first, then decrements
console.log("Postfix a--:", a--); // Output: 6 (returns a, then a becomes 5)
console.log("After a--:", a); // Output: 5

// Reset a for clarity
a = 5;

// 3. Prefix Increment (++a): Increments first, then returns new value
console.log("Prefix ++a:", ++a); // Output: 6 (a becomes 6, then returns 6)
console.log("After ++a:", a); // Output: 6

// 4. Prefix Decrement (--a): Decrements first, then returns new value
console.log("Prefix --a:", --a); // Output: 5 (a becomes 5, then returns 5)
console.log("After --a:", a); // Output: 5


